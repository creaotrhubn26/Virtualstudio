export function getGelById(id: string): GelDefinition | undefined {
  return GEL_LIBRARY.find(g => g.id === id);
}

export function getGelsByCategory(category: GelCategory): GelDefinition[] {
  return GEL_LIBRARY.filter(g => g.category === category);
}

export function getGelsByTag(tag: string): GelDefinition[] {
  return GEL_LIBRARY.filter(g => g.tags.includes(tag));
}

export function searchGels(query: string): GelDefinition[] {
  const q = query.toLowerCase();
  return GEL_LIBRARY.filter(g =>
    g.name.toLowerCase().includes(q) ||
    g.nameNo.toLowerCase().includes(q) ||
    g.filterNumber.includes(q) ||
    g.tags.some(t => t.includes(q))
  );
}
