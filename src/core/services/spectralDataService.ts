/** Planckian (blackbody) SPD normalised to 1.0 at peak. */
export function generatePlanckianSPD(cct: number): number[] {
  const c2 = 1.4388e-2; // m·K  (second radiation constant)
  const spd = WAVELENGTHS.map((nm) => {
    const lam = nm * 1e-9;
    return 1 / (Math.pow(lam, 5) * (Math.exp(c2 / (lam * cct)) - 1));
  });
  const peak = Math.max(...spd);
  return spd.map((v) => v / peak);
}

